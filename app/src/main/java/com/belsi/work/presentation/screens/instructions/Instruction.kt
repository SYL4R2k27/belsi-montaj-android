package com.belsi.work.presentation.screens.instructions

import androidx.compose.material.icons.Icons
import androidx.compose.material.icons.filled.*
import androidx.compose.ui.graphics.vector.ImageVector

/**
 * Пункты чек-листа подготовки к смене.
 * Аналог AppState.Instruction в iOS-версии.
 */
enum class Instruction(
    val title: String,
    val subtitle: String,
    val hintText: String,
    val icon: ImageVector
) {
    PREP(
        title = "Подготовка инструмента",
        subtitle = "Проверь наличие и исправность",
        hintText = """
Перед началом смены убедись, что весь необходимый инструмент при тебе и в рабочем состоянии.

Что проверить:
• Основной инструмент (дрель, шуруповёрт, перфоратор)
• Расходные материалы (свёрла, биты, дюбели)
• Измерительные приборы (рулетка, уровень)
• Средства индивидуальной защиты (перчатки, очки)

Если чего-то не хватает — сообщи бригадиру до начала смены.
        """.trimIndent(),
        icon = Icons.Default.Build
    ),

    SAFETY(
        title = "Техника безопасности",
        subtitle = "Ознакомься с правилами объекта",
        hintText = """
Безопасность — приоритет номер один на любом объекте.

Обязательные правила:
• Используй средства индивидуальной защиты (СИЗ)
• Соблюдай правила работы на высоте
• Не работай с неисправным электрооборудованием
• Знай расположение аптечки и огнетушителя
• При обнаружении опасности — немедленно сообщи бригадиру

Нарушение техники безопасности может привести к отстранению от работы.
        """.trimIndent(),
        icon = Icons.Default.Shield
    ),

    START_PHOTO(
        title = "Начальное фото объекта",
        subtitle = "Сделай фото до начала работы",
        hintText = """
Перед началом работы необходимо зафиксировать текущее состояние объекта.

Требования к фото:
• Общий план объекта / рабочей зоны
• Фото должно быть чётким и хорошо освещённым
• Объект должен быть полностью виден в кадре
• Фотографируй ДО начала любых работ

Это фото служит подтверждением состояния объекта до начала смены и защищает тебя в случае спорных ситуаций.
        """.trimIndent(),
        icon = Icons.Default.CameraAlt
    ),

    HOURLY_DOCS(
        title = "Почасовая отчётность",
        subtitle = "Фото каждый час — обязательно",
        hintText = """
В течение смены система будет напоминать о необходимости сделать фото каждый час.

Правила почасовой отчётности:
• Фото делается каждый час работы
• На фото должен быть виден прогресс работ
• Фото загружается через приложение в соответствующий временной слот
• Пропуск фото может привести к неоплате часа

Советы:
• Включи уведомления, чтобы не пропустить время
• Если работа не изменилась визуально — сфотографируй с другого ракурса
• При проблемах с загрузкой — фото сохранится локально и отправится позже
        """.trimIndent(),
        icon = Icons.Default.AccessTime
    );
}
